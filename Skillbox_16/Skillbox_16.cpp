#include <iostream>
using namespace std;

/*Задание 8. Зарплата (дополнительное задание)
Что нужно сделать
В отделе маркетинга работают три сотрудника. 
Их зарплаты могут быть как разными, так и одинаковыми. 
Напишите программу, которая вычисляет разницу между самой высокой и самой низкой зарплатами сотрудников, 
а также среднее арифметическое зарплат всех сотрудников отдела.

Пример

Введите зарплату первого сотрудника: 45 рублей
Введите зарплату второго сотрудника: 45 рублей
Введите зарплату третьего сотрудника: 45 рублей
-----Считаем-----
Самая высокая зарплата в отделе: 45 рублей
Разница между самой высокой и самой низкой зарплатой в отделе: 0 рублей
Средняя зарплата в отделе: 45 рублей

Что оценивается
Правильность нахождения разницы зарплат.
Оптимальность нахождения разницы зарплат.*/

int main()
{
	setlocale(LC_ALL,"ru");

	int salary1, salary2, salary3;
	cout << "Введите зарплату первого сотрудника: ";
	cin >> salary1;
	cout << "Введите зарплату второго сотрудника: ";
	cin >> salary2;
	cout << "Введите зарплату третьего сотрудника: ";
	cin >> salary3;

	int max_salary, min_salary;

	// находим максимальную зарплату
	if (salary1 > salary2 && salary1 > salary3)
	{
		max_salary = salary1;
	}
	else if (salary2 > salary1 && salary2 > salary3)
	{
		max_salary = salary2;
	}
	else
	{
		max_salary = salary3;
	}

	// находим минимальную зарплату
	if (salary1 < salary2 && salary1 < salary3)
	{
		min_salary = salary1;
	}
	else if (salary2 < salary1 && salary2 < salary3)
	{
		min_salary = salary2;
	}
	else
	{
		min_salary = salary3;
	}

	// вычисляем разницу между максимальной и минимальной зарплатами
	int diff = max_salary - min_salary;

	// вычисляем среднюю зарплату
	double avg_salary = (salary1 + salary2 + salary3) / 3.0;

	cout << endl;

	// выводим результаты
	cout << "Самая высокая зарплата в отделе: " << max_salary << " рублей" << endl;
	cout << "Самая низкая зарплата в отделе: " << min_salary << " рублей" << endl;
	cout << "Разница между самой высокой и самой низкой зарплатой в отделе: " << diff << " рублей" << endl;
	cout << "Средняя зарплата в отделе: " << avg_salary << " рублей" << endl;

	return 0;
}