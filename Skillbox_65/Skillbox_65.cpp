#include <iostream>
#include <vector>

using namespace std;

/*Задача 2
Что нужно сделать
С помощью списка инициализации задаётся вектор цен на продукты (дробными
числами). Таким же образом с помощью списка инициализации задаётся вектор покупок (в виде индексов), 
которые совершает человек. Необходимо вывести на экран итоговую стоимость покупок. 
Вектор цен и вектор покупок можно задать напрямую в коде.*/

int main() 
{
	setlocale(LC_ALL,"ru");

	// Вектор цен
	vector<float> prices{ 2.5, 4.25, 3.0, 10.0 };

	// Вектор покупок
	vector<int> items{ 1, 1, 0, 3 };

	// Переменная для хранения суммарной стоимости
	float totalCost = 0.0;

	// Подсчет суммарной стоимости покупок
	for (int item : items) 
	{
		// Проверка на корректность индекса
		if (item >= 0 && item < prices.size()) 
		{
			totalCost += prices[item];
		}
		else 
		{
			cout << "Ошибка: некорректный индекс товара!" << endl;
			return 1;  // Выход из программы с кодом ошибки
		}
	}

	// Вывод вектора цен
	cout << "Вектор цен: {";
	for (float price : prices) 
	{
		cout << price << ", ";
	}
	cout << "}" << endl;

	// Вывод вектора покупок
	cout << "Вектор покупок: {";
	for (int item : items) 
	{
		cout << item << ", ";
	}
	cout << "}" << endl;

	// Вывод итоговой стоимости покупок
	cout << "Суммарная стоимость покупок: " << totalCost << endl;

	return 0;
}